def draw_maze(win, cell_size):
    for row in range(len(maze)):
        for col in range(len(maze[row])):
            if maze[row][col] == '#':
                pygame.draw.rect(win, (255, 255, 255), (col * cell_size, row * cell_size, cell_size, cell_size))
